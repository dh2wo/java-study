
public class SwitchTest1 {
	public static void main(String[] args) {
		int rank = 1;
		
		switch(rank) {
		case 1 : 
			System.out.println("1등입니다!");
			break;
		case 2 :
			System.out.println("2등입니다!");
			break;
		case 3 :
			System.out.println("3등입니다!");
			break;
		default :
			System.out.println("파이팅!");			
		}		
	}
}
